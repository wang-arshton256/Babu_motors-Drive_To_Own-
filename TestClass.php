

====>Larvel File Structure =====>

1 . App/Http/Controllers  ==>Controllers
2 . routes/web.php        ==> Routes
3 . App/Models            ==> Models
4 . public                ==> Is where css/js/img/fonts are stored
5 . resources/views       ==> Actual HTML files
6 . database/migrations   ==> Database files


Laravel is Model View Controller Architecture  (MVC)

--> App/Http/Controllers  ==>Controllers

-->  App/Models            ==> Models

--> resources/views

in laravel routes connect Controllers to models to views

